                        <li>Notificar imediatamente o banco em caso de perda, roubo ou suspeita de uso não autorizado</li>
                        <li>Utilizar senhas fortes e alterá-las periodicamente</li>
                        <li>Não compartilhar credenciais com terceiros</li>
                    </ul>
