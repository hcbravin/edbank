                <div class="termos-section" id="cadastro">
                    <h3>4. Cadastro e Conta</h3>
                    <h5 class="mt-4">4.1 Elegibilidade</h5>
                    <p>Para abrir uma conta no Education Bank, você deve:</p>
                    <ul class="termos-list">
                        <li>Ser maior de 18 anos ou emancipado</li>
                        <li>Possuir documento de identificação válido</li>
                        <li>Fornecer informações verdadeiras e atualizadas</li>
                        <li>Residir em território nacional</li>
                    </ul>

                    <h5 class="mt-4">4.2 Verificação de Identidade</h5>
                    <p>Conforme exigido pela legislação, realizamos verificação de identidade que pode incluir consulta a bases de dados públicas e privadas.</p>

                    <h5 class="mt-4">4.3 Responsabilidade pelas Credenciais</h5>
                    <p>Você é responsável por:</p>
                    <ul class="termos-list">
