                        <li>Cumprimento de obrigações legais</li>
                        <li>Prevenção à fraude e lavagem de dinheiro</li>
                        <li>Melhoria da experiência do cliente</li>
                    </ul>

                    <h5 class="mt-4">5.3 Compartilhamento de Dados</h5>
                    <p>Podemos compartilhar seus dados com:</p>
                    <ul class="termos-list">
                        <li>Autoridades regulatórias, quando exigido por lei</li>
                        <li>Parceiros comerciais, mediante seu consentimento</li>
                        <li>Empresas do mesmo grupo econômico</li>
                    </ul>

