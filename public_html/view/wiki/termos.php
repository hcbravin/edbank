                <div class="termos-section" id="servicos">
                    <h3>3. Serviços Bancários</h3>
                    <h5 class="mt-4">3.1 Serviços Oferecidos</h5>
                    <p>O Education Bank oferece os seguintes serviços, sujeitos à disponibilidade e regulamentação:</p>
                    <ul class="termos-list">
                        <li>Contas correntes e poupança</li>
                        <li>Transferências bancárias (TED, DOC e PIX)</li>
                        <li>Pagamento de contas e tributos</li>
                        <li>Extrato e consulta de saldo</li>
                        <li>Cartões de débito e crédito</li>
                        <li>Investimentos financeiros</li>
                        <li>Empréstimos e financiamentos</li>
                    </ul>

                    <h5 class="mt-4">3.2 Disponibilidade dos Serviços</h5>
                    <p>Nos esforçamos para manter os serviços disponíveis 24 horas por dia, 7 dias por semana. No entanto, podemos interromper o acesso para manutenção, atualizações ou por motivos de força maior.</p>

                    <h5 class="mt-4">3.3 Tarifas e Taxas</h5>
                    <p>Alguns serviços podem estar sujeitos a tarifas, que serão comunicadas previamente ao cliente. As tarifas vigentes estão disponíveis em nossas agências e no site oficial.</p>
