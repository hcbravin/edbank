                        <li>Reportar transações não autorizadas em até 24 horas</li>
                        <li>Manter seus dados cadastrais atualizados</li>
                        <li>Utilizar os serviços de forma legal e ética</li>
                    </ul>

                    <h5 class="mt-4">6.3 Limitação de Responsabilidade</h5>
                    <p>Não nos responsabilizamos por:</p>
                    <ul class="termos-list">
                        <li>Perdas decorrentes de força maior ou caso fortuito</li>
                        <li>Problemas técnicos do dispositivo do cliente</li>
                        <li>Uso indevido das credenciais pelo cliente</li>
                        <li>Ações de terceiros não relacionadas aos nossos sistemas</li>
                    </ul>
