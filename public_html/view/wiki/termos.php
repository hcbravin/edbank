                    <p>O Education Bank se responsabiliza por:</p>
                    <ul class="termos-list">
                        <li>Manter a segurança dos sistemas e infraestrutura</li>
                        <li>Garantir a confidencialidade dos dados do cliente</li>
                        <li>Prestar serviços com diligência e profissionalismo</li>
                        <li>Cumprir a legislação bancária aplicável</li>
                    </ul>

                    <h5 class="mt-4">6.2 Do Cliente</h5>
                    <p>O cliente se responsabiliza por:</p>
                    <ul class="termos-list">
